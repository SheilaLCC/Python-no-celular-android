#faca um programa que leia um número inteiro 
#e mostre na tela o seu sucessor e o seu
#antecessor
n=int(input ('\033[7;40m Digite um numero: \033[m  '))
a=n-1
p=n+1
print('Para o número que foi digitado: \033[4;31m{}\033[m , o anterior é: \033[4;32m{}\033[m e o número posterior é: \033[4;33m{}\033[m'.format (n,a,p))



